class NumberPatternPrinter {
    public static void main(String[] args) {
        System.out.println("\nWelcome to the number pattern printer\n");

        System.out.println("1    ");
        System.out.println("2 3   ");
        System.out.println("4 5 6  ");
        System.out.println("7 8 9 10\n");

        System.out.println("Thank you for seeing this");
    }
}